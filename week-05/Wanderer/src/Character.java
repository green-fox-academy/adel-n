public class Character {
  int maxHP;
  int currentHP;
  int defendPoint;
  int strikePoint;

  public Character(int maxHP, int currentHP, int defendPoint, int strikePoint) {
    this.maxHP = maxHP;
    this.currentHP = currentHP;
    this.defendPoint = defendPoint;
    this.strikePoint = strikePoint;
  }

  public Character() {
  }
}
